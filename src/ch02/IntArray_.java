package ch02;

// 실습 2-1 (코드 리뷰 ver)
// 배열 생성, 데이터 저장 및 전체 출력

// IntArray_ 클래스 선언 : 정수형 배열 사용 예제(상세 주석)를 보여주는 클래스
class IntArray_ {
    // main 메서드 - 프로그램의 진입점 (Entry Point)
    // JVM 이 프로그램을 실행할 때, 가장 먼저 호출되는 메서드
    public static void main(String[] args) {
        // 정수형 배열 선언 및 생성
        // int[] : 정수형 배열 타입 선언
        // a : 배열 변수의 이름
        // new int[5] : 크기가 5인 새로운 정수형 배열 객체 생성
        // 생성과 동시에 모든 요소가 기본값 0으로 자동 초기화됨
        // 메모리 상태: [0, 0, 0, 0, 0]

        int[] a = new int[5]; // 이 순간 메모리 상태: [0, 0, 0, 0, 0]

        // 배열의 1번 인덱스(두 번째 위치)에 37 저장
        // 배열 인덱스는 0부터 시작하므로 a[1]은 두 번째 요소
        // 메모리 상태: [0, 37, 0, 0, 0]
        a[1] = 37;

        // 배열의 2번 인덱스(세 번째 위치)에 51 저장
        // 메모리 상태: [0, 37, 51, 0, 0]
        a[2] = 51;

        // 배열의 4번 인덱스(다섯 번째 위치)에 51 저장
        // a[1] * 2 : a[1]의 값(37)에 2를 곱한 결과(74)
        // 메모리 상태: [0, 37, 51, 0, 74]
        a[4] = a[1] * 2;

        // 빈 줄 출력 - 가독성을 위한 공백 라인
        System.out.println();

        // for 반복문 - 배열의 모든 요소를 순차적으로 출력
        // 초기화 : int i = 0 (반복 변수 i를 0으로 시작
        // 조건  : i < a.length (i가 배열 길이보다 작을 때까지 반복)
        // 증감  : i++ (매 반복마다 i를 1씩 증가)
        // a.length : 배열의 길이를 반환하는 속성(여기서는 5)
        for (int i = 0; i < a.length; i++) {
            // 각 배열 요소를 'a[인덱스] = 값' 형태로 출력
            // 문자열 연결 연산자(+)를 사용하여 문자열과 숫자를 결합
            // i : 현재 인덱스 값
            // a[i] : 현재 인덱스에 저장된 배열 요소의 값
            System.out.println("a[" + i + "] = " + a[i]);
        }

        // 출력 결과 :
        // a[0] = 0
        // a[1] = 37
        // a[2] = 51
        // a[3] = 0
        // a[4] = 74
    }
}
